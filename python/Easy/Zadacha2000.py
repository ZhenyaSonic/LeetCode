"""
Дана строка с индексом 0 word и символ ch, перевернуть сегмент , wordкоторый начинается с индекса 0и заканчивается индексом первого вхождения (ch включительно ) . Если символ chне существует в word, ничего не делать.

Например, если word = "abcdefd"и ch = "d", то следует поменять местами сегмент, который начинается с 0и заканчивается на 3( включительно ). Результирующая строка будет ."dcbaefd"
"""


class Solution:
    def reversePrefix(self, word: str, ch: str) -> str:
        index = word.find(ch)

        # Если символ не найден, возвращаем строку без изменений
        if index == -1:
            return word

        # Переворачиваем сегмент от 0 до index включительно
        reversed_segment = word[:index + 1][::-1]

        # Формируем новую строку
        result = reversed_segment + word[index + 1:]

        return result
